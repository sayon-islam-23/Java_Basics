public class operators {
    public static void main(String[] args) {
        int a = 10;
        int b = 20;
        System.out.print("The summation of the numbers ");
        System.out.println(a+b);
        System.out.print("The difference of the numbers ");
        System.out.println(b-a);
        System.out.print("The multiplication of the numbers ");
        System.out.println(b*a);
        System.out.print("The division of the numbers ");
        System.out.println(b/a);
        System.out.print("The reminder of the numbers ");
        System.out.println(b%a);
        System.out.println(a++);
        System.out.println(a--);
        System.out.println(++a);
        System.out.println(--a);
        System.out.println(a += 3); // a+3 = a
        System.out.println(a =+ 3); // a = a+3
    }
}




